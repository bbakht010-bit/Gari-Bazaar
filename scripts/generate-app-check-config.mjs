/**
 * Writes app-check-config.js from env (CI/local deploy) or keeps manual edits.
 * Env: GARI_BAZAAR_APP_CHECK_SITE_KEY, GARI_BAZAAR_APP_CHECK_DEBUG_TOKEN (optional)
 */
import fs from "node:fs";
import path from "node:path";

const repoRoot = path.resolve(process.cwd());
const outPath = path.join(repoRoot, "app-check-config.js");
const siteKey = String(process.env.GARI_BAZAAR_APP_CHECK_SITE_KEY || "").trim();
const debugToken = String(process.env.GARI_BAZAAR_APP_CHECK_DEBUG_TOKEN || "").trim();

let existingSiteKey = "";
let existingDebugToken = "";
if (fs.existsSync(outPath)) {
  const current = fs.readFileSync(outPath, "utf8");
  const siteMatch = current.match(/GARI_BAZAAR_APP_CHECK_SITE_KEY\s*=\s*"([^"]*)"/);
  const debugMatch = current.match(/GARI_BAZAAR_APP_CHECK_DEBUG_TOKEN\s*=\s*"([^"]*)"/);
  if (siteMatch) existingSiteKey = siteMatch[1];
  if (debugMatch) existingDebugToken = debugMatch[1];
}

const finalSiteKey = siteKey || existingSiteKey;
const finalDebugToken = debugToken || existingDebugToken;

const contents = `/**
 * Generated by \`npm run generate:app-check-config\` or edited manually.
 * Firebase Console → App Check → Web app → reCAPTCHA v3 site key.
 */
window.GARI_BAZAAR_APP_CHECK_SITE_KEY = ${JSON.stringify(finalSiteKey)};
window.GARI_BAZAAR_APP_CHECK_DEBUG_TOKEN = ${JSON.stringify(finalDebugToken)};
`;

fs.writeFileSync(outPath, contents, "utf8");

const enforcePath = path.join(repoRoot, "functions", ".app-check-enforce");
const enforce = finalSiteKey.length > 20;
fs.writeFileSync(enforcePath, enforce ? "1" : "0", "utf8");

console.log(
  enforce
    ? "App Check config: site key present — callable enforcement will be enabled on Functions deploy."
    : "App Check config: no site key yet — callable enforcement stays off until you add one."
);
